class QueryRewriter:
    """Переформулирует запрос в юридические ключевые слова для векторного поиска."""

    def __init__(self, llm_client):
        self.llm = llm_client

    def rewrite(self, query: str) -> str:
        prompt = (
            "Проанализируй запрос пользователя.\n"
            "1. Если это простое приветствие, благодарность или вопрос не по праву (например: 'привет', 'ты кто'), просто верни этот же текст КАК ЕСТЬ, без изменений.\n"
            "2. Если это юридический вопрос или описание ситуации, переведи его на строгий язык УК РФ, ГК РФ или ТК РФ. ПОЛНОСТЬЮ ИСКЛЮЧАЙ места (магазин, улица) и типы предметов (рыба, сырки, продукты, техника). Оставляй ТОЛЬКО абстрактные юридические термины.\n"
            "ВАЖНО: НЕ ПИШИ вопросы. Напиши ТОЛЬКО сухую выжимку терминов для поисковой системы.\n"
            "Пример 1: 'я украла сырки в магазине' -> 'кража тайное хищение чужого имущества'\n"
            "Пример 2: 'хочу вернуть телефон, он сломался' -> 'потребительский спор возврат товара'\n"
            "Пример 3: 'хочу уволиться сразу без двух недель' -> 'расторжение трудового договора по инициативе работника без отработки'\n\n"
            f"Запрос: {query}\n\n"
            "Результат:"
        )
        try:
            rewritten = self.llm.generate(
                messages=[{"role": "user", "content": prompt}],
                max_tokens=100
            )
            return rewritten.strip()
        except Exception as e:
            print(f"Не удалось переформулировать запрос: {e}")
            return query
