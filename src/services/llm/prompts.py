from typing import List


SYSTEM_PROMPT = """Ты — юридический консультант, специализирующийся на законодательстве Российской Федерации.
Твоя задача — отвечать на вопросы пользователя, основываясь **исключительно** на предоставленном контексте из нормативных документов.
Если в контексте нет ответа, честно скажи, что не можешь ответить на вопрос, и предложи обратиться к профессиональному юристу.
Не придумывай информацию. В ответе обязательно указывай ссылки на использованные источники (названия документов)."""


def build_rag_prompt(query: str, context_chunks: List[str]) -> str:
    context_text = "\n\n---\n\n".join(context_chunks)
    return f"""Контекст из документов:
{context_text}

Вопрос пользователя: {query}

Ответ (со ссылками на источники):"""
